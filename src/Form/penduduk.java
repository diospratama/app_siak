    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        try
        {
            Statement st = (Statement)Koneksi.koneksi().createStatement();
            String search = txt_cari_kk.getText();
            ResultSet rs = st.executeQuery("SELECT * FROM data_kk WHERE no_kk like '%"+search+"%' OR nama_kepala_keluarga like '%"+search+"%'");
           // DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();

            tabelModel_dialog.setRowCount(0);
            String [] data = new String[2];
            int i = 1;

            while(rs.next())
            {
                data[0] = rs.getString("no_kk");
                data[1] = rs.getString("nama_kepala_keluarga");
                
                tabelModel_dialog.addRow(data);
                i++;
            }
            rs.close();
            //txtcari.setText("");
        }
        catch(Exception ex)  {
            JOptionPane.showMessageDialog(null,"Data yang Anda cari Tidak dapat Ditemukan");
            System.err.println("error (search) : " +ex);
        }
    }//GEN-LAST:event_jButton6ActionPerformed

