public void autonumber() {
        try {
            Statement st =(Statement)Koneksi.koneksi().createStatement();
            String query_bukaTabel="select right(id_kematian,5) as no_urut from data_kematian GROUP BY id_kematian ASC";
            java.sql.ResultSet line_result = st.executeQuery(query_bukaTabel);
            if (line_result.first() == false) {
                txt_id.setText("1");
            } else {
                line_result.last();
                int no = line_result.getInt(1) + 1;
                
                String nomor = String.valueOf(no);
                int oto = nomor.length();
                    String x=nomor;
                txt_id.setText(""+x);
            }
        } catch (Exception e) {
            e.printStackTrace();//penanganan masalah
        }
    }
      
    
  
