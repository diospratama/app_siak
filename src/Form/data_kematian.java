    SimpleDateFormat time = new SimpleDateFormat("HH:mm:ss");
    spin_time.setValue(time.parseObject(jam));
    
    txt_area.setText(tabel_kematian.getValueAt(row, 4).toString());
    tempat_kematian.setText(tabel_kematian.getValueAt(row, 5).toString());
    
    if(tabel_kematian.getValueAt(row, 6).toString().equals("Dokter")){
       cmb_ket.setSelectedItem("Dokter");
    }
    
    else if(tabel_kematian.getValueAt(row, 6).toString().equals("Tenaga Kesehatan")){
       cmb_ket.setSelectedItem("Tenaga Kesehatan");
    }
    
    else if(tabel_kematian.getValueAt(row, 6).toString().equals("Kepolisian")){
       cmb_ket.setSelectedItem("Kepolisian");
    }
    
    else if(tabel_kematian.getValueAt(row, 6).toString().equals("Lainnya")){
       cmb_ket.setSelectedItem("Lainnya");
    }
    
    
    txt_ayah.setText(tabel_kematian.getValueAt(row, 7).toString());
    txt_ibu.setText(tabel_kematian.getValueAt(row, 8).toString());
    txt_lapor.setText(tabel_kematian.getValueAt(row, 9).toString());
